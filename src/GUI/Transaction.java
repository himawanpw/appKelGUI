    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model =(DefaultTableModel) table_Show.getModel();
        String s1 = txt_Harga.getText();
        String s2 = txt_quantity.getText();
        
        int a = Integer.parseInt(s1);
        double b = Double.parseDouble(s2);
        double c = a*b;
        
        String result = String.valueOf(c);
        model.addRow(new Object[]{txt_quantity.getText(),txt_Harga.getText(),cbx_name.getSelectedItem(),result});
        getsum();
    }//GEN-LAST:event_btn_addActionPerformed
    public void getsum(){
        double sum=0;
          for(int i=0; i < table_Show.getRowCount(); i++){
              sum = sum + Double.parseDouble(table_Show.getValueAt(i,3).toString());
          }
          txt_total.setText(Double.toString(sum));
    }
    
    public void transactionUpdate(){
        String kelengkeng = (String) cbx_name.getSelectedItem();
        String sql = "insert into transaksi(id,kelengkeng,harga_bayar,jumlah,tanggal) "
                + "Values ("+txt_noBill.getText()+",'"+kelengkeng+"',"+txt_total.getText()+","+txt_quantity.getText()+",'"+ttx_Date.getText()+"')";
        try {
            connection = ConnectionConfig.getConnection();
            statement = connection.createStatement();
            statement.execute(sql);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
