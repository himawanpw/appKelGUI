    public void fillCombo(){
        String sql="select * from kelengkeng";
        try {
            connection = ConnectionConfig.getConnection();
            preparedStatement=connection.prepareCall(sql);
            resultSet = preparedStatement.executeQuery();
            while(resultSet.next()){
                String name = resultSet.getString("jenis");
                cbx_name.addItem(name);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public static  String DateFormat = "yyyy-MM-dd";
    public void showDate(){
      Calendar cal= Calendar.getInstance();
      SimpleDateFormat format = new SimpleDateFormat(DateFormat);
      ttx_Date.setText(format.format(cal.getTime()));
    }
    public void transaction(){
        String sql = "select count(*) from transaksi";
        try {
            connection = ConnectionConfig.getConnection();
            preparedStatement=connection.prepareStatement(sql);
            resultSet=preparedStatement.executeQuery();
            while(resultSet.next()){
                int g=Integer.parseInt(resultSet.getString(1));
                g=g+1;
                txt_noBill.setText(String.valueOf(g));
            }
        } catch (Exception e) {
        }
    }
