    public ArrayList<Kelengkeng> search(String ValSearch){
        ArrayList<Kelengkeng> listSearch = new ArrayList<Kelengkeng>();
        
            try {
                connection = ConnectionConfig.getConnection();
                statement=connection.createStatement();
                String sql = "select * from transaksi where kelengkeng ='"+ValSearch+"';";
                resultSet=statement.executeQuery(sql);
                Kelengkeng kelengkeng;
                while(resultSet.next()){
                    kelengkeng= new Kelengkeng(resultSet.getDouble("harga_bayar"),resultSet.getInt("id"),resultSet.getString("kelengkeng"),resultSet
                    .getString("tanggal"),resultSet.getInt("jumlah"));
                    listSearch.add(kelengkeng);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        return   listSearch;  
    }
