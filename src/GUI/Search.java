    public void search(){
        ArrayList <Kelengkeng> kelengkeng = search(txt_search.getText());
            DefaultTableModel model = (DefaultTableModel)tbl_search.getModel();
            Object[] row =new Object [5];
            for(int i=0;i < kelengkeng.size(); i++){
                row[0] = kelengkeng.get(i).getId();
                row[1] = kelengkeng.get(i).getKelengkeng();
                row[2] = kelengkeng.get(i).getQuan();
                row[3] = kelengkeng.get(i).getBayar();
                row[4] = kelengkeng.get(i).getTanggal();
                model.addRow(row);
            }
            tbl_search.setModel(model);
    }
